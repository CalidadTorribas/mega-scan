  get(name, customClass = '') {
    if (!this.icons[name]) {
      console.warn(`Icono '${name}' no encontrado`);
      return '';
    }
    
    return customClass ? 
      this.icons[name].replace(/w-\d+ h-\d+/, customClass) : 
      this.icons[name];
  }

  add(name, svg) {
    this.icons[name] = svg;
  }

  list() {
    return Object.keys(this.icons);
  }
